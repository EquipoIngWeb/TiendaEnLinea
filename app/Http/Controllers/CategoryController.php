	public function printF($list)
	{
		$string = "<ul>";
		foreach ($list as $category) {
			$flat= false;
			foreach ($list as $brother) {
				if ($category->isParent($brother->id)) {
					$flat=true;
					break;
				}
			}
			if (!$flat) {
				$string.= "<li> $category->name</li>";
				$string.= $this->printF($category->children()->get());
			}
		}
		return $string.= "</ul>";
	}
